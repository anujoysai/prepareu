function payment_form()
{
	var email=$('#get_started_email').val();
	var val_email=ValidateEmail(email) 
	if(email=="")
	{
		$('#get_started_email').css('border','1px solid #f00');
		$('#error-msg').html('Please enter email address');
	}
	else if(!val_email)
	{
		$('#get_started_email').css('border','1px solid #f00');
		$('#error-msg').html('Please enter a valid email address');
	}
	else
	{
		$('#myModal').modal('show');
		$('#error-msg').html('');
		$('#get_started_email').css('border','0px');
		$('#user_email').val($('#get_started_email').val());
	}
}
